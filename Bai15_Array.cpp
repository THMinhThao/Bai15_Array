#include <iostream>
using namespace std;
int main()
{
   // Create an arry of strings
	string cars[5] = { "Volvo", "BMW", "Ford", "Mazda", "tesla" };

	// Loop through strings 
	for (int i = 0; i < 5; i++)
	{
		cout << cars[i] << "\n";
	}
}


